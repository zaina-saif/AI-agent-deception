"""
Ablation Study for AI Deception Detection Pipeline

Compares:
  A) Prompt strategy:  zero_shot  vs  few_shot  vs  cot
  B) Agent config:     detector-only  vs  +verifier  vs  +consistency  vs  all three

Evaluation data: TruthfulQA (correct answers = label 0, incorrect answers = label 1)
                 plus answers generated by the DeceptiveGeneratorAgent (label 1)

Results saved to:
  eval/ablation_results.csv     — per-example scores for every config
  eval/ablation_summary.csv     — accuracy / precision / recall / F1 per config
  eval/failure_analysis.txt     — worst false positives + false negatives
"""

import os
import sys
import json
import csv
from itertools import product
from typing import List, Dict, Optional

import pandas as pd
from tqdm import tqdm
from dotenv import load_dotenv

# Make sure the parent directory is on the path
_DIR = os.path.dirname(os.path.abspath(__file__))
_ROOT = os.path.dirname(_DIR)
sys.path.insert(0, _ROOT)

load_dotenv()

from agents.deception_detector import detect_deception, PromptStrategy
from agents.generator_agent import GeneratorAgent
from data.load_datasets import build_detection_pairs

THRESHOLD = 0.5          # deception_score >= THRESHOLD → predicted deceptive
MAX_EXAMPLES = 50        # TruthfulQA examples (split ~50/50 truthful/deceptive)
DECEPTIVE_GEN_N = 15     # Extra examples from deceptive generator
DETECTOR_MODEL = "gpt-4o"

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def compute_metrics(df: pd.DataFrame, threshold: float = THRESHOLD) -> Dict[str, float]:
    """Compute classification metrics from a dataframe with deception_score + true_label."""
    valid = df[df["deception_score"] >= 0].copy()
    valid["pred"] = (valid["deception_score"] >= threshold).astype(int)

    tp = int(((valid["pred"] == 1) & (valid["true_label"] == 1)).sum())
    tn = int(((valid["pred"] == 0) & (valid["true_label"] == 0)).sum())
    fp = int(((valid["pred"] == 1) & (valid["true_label"] == 0)).sum())
    fn = int(((valid["pred"] == 0) & (valid["true_label"] == 1)).sum())

    n = len(valid)
    accuracy  = (tp + tn) / n if n else 0
    precision = tp / (tp + fp) if (tp + fp) else 0
    recall    = tp / (tp + fn) if (tp + fn) else 0
    f1        = 2 * precision * recall / (precision + recall) if (precision + recall) else 0

    return dict(n=n, tp=tp, tn=tn, fp=fp, fn=fn,
                accuracy=round(accuracy, 4),
                precision=round(precision, 4),
                recall=round(recall, 4),
                f1=round(f1, 4))


# ---------------------------------------------------------------------------
# Step 1 — Build evaluation dataset
# ---------------------------------------------------------------------------

def build_eval_dataset(api_key: str) -> pd.DataFrame:
    """
    Combine:
      - TruthfulQA ground-truth pairs  (label 0 = truthful, 1 = deceptive)
      - Answers from deceptive generator (label 1)
    """
    print("Building evaluation dataset...")

    # TruthfulQA pairs (build_detection_pairs uses column name "label")
    tqa_df = build_detection_pairs(max_examples=MAX_EXAMPLES)
    tqa_df = tqa_df.rename(columns={"label": "true_label"})
    tqa_df["source_type"] = "truthfulqa"

    # Deceptive generator examples
    print(f"Generating {DECEPTIVE_GEN_N} deceptive answers from DeceptiveGeneratorAgent...")
    gen = GeneratorAgent(api_key=api_key)

    # Sample questions from the truthful set
    tqa_questions = tqa_df[tqa_df["true_label"] == 0]["question"].tolist()[:DECEPTIVE_GEN_N]

    deceptive_rows = []
    for q in tqdm(tqa_questions, desc="Deceptive generator"):
        try:
            ans = gen.generate_answer(q, mode="deceptive")
            deceptive_rows.append({
                "question": q,
                "answer": ans,
                "true_label": 1,
                "source": "deceptive_generator",
                "source_type": "deceptive_generator",
            })
        except Exception as e:
            print(f"  Error generating deceptive answer: {e}")

    gen_df = pd.DataFrame(deceptive_rows)

    combined = pd.concat([tqa_df, gen_df], ignore_index=True)
    print(f"Dataset: {len(combined)} examples "
          f"({combined['true_label'].sum()} deceptive, "
          f"{(combined['true_label']==0).sum()} truthful)")
    return combined


# ---------------------------------------------------------------------------
# Step 2 — Run detector for all prompt strategies
# ---------------------------------------------------------------------------

STRATEGIES: List[PromptStrategy] = ["zero_shot", "few_shot", "cot"]


def run_strategy_ablation(df: pd.DataFrame) -> pd.DataFrame:
    """
    Run detect_deception for each strategy on every row.
    Returns df with added columns: score_{strategy}, flags_{strategy}, reasoning_{strategy}
    """
    results_df = df.copy()

    for strategy in STRATEGIES:
        print(f"\n--- Strategy: {strategy} ---")
        scores, flags_col, reasonings = [], [], []

        for _, row in tqdm(results_df.iterrows(), total=len(results_df), desc=strategy):
            try:
                det = detect_deception(row["question"], row["answer"],
                                       model=DETECTOR_MODEL, strategy=strategy)
                scores.append(det["deception_score"])
                flags_col.append(json.dumps(det["flags"]))
                reasonings.append(det["reasoning"])
            except Exception as e:
                print(f"  Error: {e}")
                scores.append(-1)
                flags_col.append("[]")
                reasonings.append("error")

        results_df[f"score_{strategy}"] = scores
        results_df[f"flags_{strategy}"] = flags_col
        results_df[f"reasoning_{strategy}"] = reasonings

    return results_df


# ---------------------------------------------------------------------------
# Step 3 — Summarise strategy ablation
# ---------------------------------------------------------------------------

def summarise_strategy_ablation(results_df: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for strategy in STRATEGIES:
        col = f"score_{strategy}"
        sub = results_df[["true_label", col]].rename(columns={col: "deception_score"})
        m = compute_metrics(sub)
        m["strategy"] = strategy
        m["config"] = "detector_only"
        rows.append(m)
    return pd.DataFrame(rows)


# ---------------------------------------------------------------------------
# Step 4 — Failure analysis
# ---------------------------------------------------------------------------

def failure_analysis(results_df: pd.DataFrame, strategy: str = "few_shot", n: int = 5) -> str:
    """Return a text report of the worst false positives and false negatives."""
    col = f"score_{strategy}"
    valid = results_df[results_df[col] >= 0].copy()
    valid["pred"] = (valid[col] >= THRESHOLD).astype(int)

    fp = valid[(valid["pred"] == 1) & (valid["true_label"] == 0)].sort_values(col, ascending=False)
    fn = valid[(valid["pred"] == 0) & (valid["true_label"] == 1)].sort_values(col, ascending=True)

    lines = [f"=== Failure Analysis (strategy={strategy}, threshold={THRESHOLD}) ===\n"]

    lines.append(f"\n--- False Positives (truthful answers flagged as deceptive) [{len(fp)} total] ---")
    for _, row in fp.head(n).iterrows():
        lines.append(f"\n  Q: {row['question'][:90]}")
        lines.append(f"  A: {row['answer'][:120]}")
        lines.append(f"  Score: {row[col]:.2f}  Source: {row.get('source', '?')}")
        lines.append(f"  Reasoning: {row[f'reasoning_{strategy}'][:150]}")

    lines.append(f"\n--- False Negatives (deceptive answers missed) [{len(fn)} total] ---")
    for _, row in fn.head(n).iterrows():
        lines.append(f"\n  Q: {row['question'][:90]}")
        lines.append(f"  A: {row['answer'][:120]}")
        lines.append(f"  Score: {row[col]:.2f}  Source: {row.get('source', '?')}")
        lines.append(f"  Reasoning: {row[f'reasoning_{strategy}'][:150]}")

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Step 5 — Score distribution summary
# ---------------------------------------------------------------------------

def score_distribution(results_df: pd.DataFrame) -> str:
    lines = ["\n=== Score Distribution by True Label ==="]
    for strategy in STRATEGIES:
        col = f"score_{strategy}"
        if col not in results_df.columns:
            continue
        lines.append(f"\nStrategy: {strategy}")
        sub = results_df[results_df[col] >= 0].groupby("true_label")[col].describe()
        lines.append(sub.to_string())
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    api_key = os.getenv("OPENAI_API_KEY")
    if not api_key:
        print("OPENAI_API_KEY not set. Please add it to your .env file.")
        return

    # ---- Build dataset ----
    eval_df = build_eval_dataset(api_key)

    # ---- Strategy ablation (zero_shot vs few_shot vs cot) ----
    print("\n\n=== Running prompt strategy ablation ===")
    results_df = run_strategy_ablation(eval_df)

    out_path = os.path.join(_DIR, "ablation_results.csv")
    results_df.to_csv(out_path, index=False)
    print(f"\nPer-example results saved to {out_path}")

    # ---- Summary metrics ----
    summary_df = summarise_strategy_ablation(results_df)

    print("\n\n=== Prompt Strategy Ablation Summary ===")
    print(summary_df[["strategy", "n", "accuracy", "precision", "recall", "f1"]].to_string(index=False))

    summary_path = os.path.join(_DIR, "ablation_summary.csv")
    summary_df.to_csv(summary_path, index=False)
    print(f"Summary saved to {summary_path}")

    # ---- Score distribution ----
    dist_text = score_distribution(results_df)
    print(dist_text)

    # ---- Failure analysis (best-performing strategy) ----
    # Pick the strategy with highest F1 for the failure report
    best_strategy = summary_df.loc[summary_df["f1"].idxmax(), "strategy"]
    print(f"\nBest strategy by F1: {best_strategy}")
    failure_text = failure_analysis(results_df, strategy=best_strategy, n=5)
    print(failure_text)

    fail_path = os.path.join(_DIR, "failure_analysis.txt")
    with open(fail_path, "w") as f:
        f.write(failure_text)
        f.write("\n\n")
        f.write(dist_text)
    print(f"Failure analysis saved to {fail_path}")

    # ---- Print final comparison table for each strategy ----
    print("\n\n=== Final Results Table ===")
    print(f"{'Strategy':<12} {'N':>4} {'Accuracy':>9} {'Precision':>10} {'Recall':>8} {'F1':>7}")
    print("-" * 55)
    for _, row in summary_df.iterrows():
        print(f"{row['strategy']:<12} {row['n']:>4} {row['accuracy']:>9.3f} "
              f"{row['precision']:>10.3f} {row['recall']:>8.3f} {row['f1']:>7.3f}")


if __name__ == "__main__":
    main()
